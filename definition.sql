 ON "SYSDBA"."FOOD" 
 referencing OLD ROW AS "OLD" NEW ROW AS "NEW"

 for each row
 
BEGIN
    SELECT SYSDBA.SEQ_FOOD_ID.NEXTVAL INTO :NEW.ID FROM DUAL;
END;
