-- 创建营养推荐表
CREATE TABLE SYSDBA.NUTRITION_RECOMMENDATION (
    ID INTEGER PRIMARY KEY,
    USER_ID INTEGER NOT NULL,
    RECOMMENDATION_DATE DATE NOT NULL,
    CALORIES INTEGER NOT NULL,
    PROTEIN DECIMAL(10,2) NOT NULL,
    CARBOHYDRATE DECIMAL(10,2) NOT NULL,
    FAT DECIMAL(10,2) NOT NULL,
    FIBER DECIMAL(10,2),
    SODIUM DECIMAL(10,2),
    RECOMMENDATION_TYPE VARCHAR(20) NOT NULL, -- 'DAILY', 'WEEKLY', 'CUSTOM'
    NOTES VARCHAR(500),
    CREATE_TIME TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT FK_NUTRITION_RECOMMENDATION_USER_ID FOREIGN KEY (USER_ID) REFERENCES SYSDBA."USER"(ID)
);